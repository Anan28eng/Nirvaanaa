// Polyfill Response for tests (minimal) and ensure static json exists
if (typeof global.Response === 'undefined') {
  global.Response = class {
    constructor(body, init = {}) {
      this._body = body;
      this.status = init.status || 200;
      this.headers = new Map(Object.entries(init.headers || {}));
    }
    async json() {
      try {
        return JSON.parse(this._body);
      } catch (e) {
        return this._body;
      }
    }
    async text() {
      return String(this._body);
    }
    async arrayBuffer() {
      return Buffer.from(String(this._body));
    }
  };
}

if (typeof global.Response.json !== 'function') {
  global.Response.json = function(body, init = {}) {
    const payload = typeof body === 'string' ? body : JSON.stringify(body);
    const headers = new Map(Object.entries(init.headers || {}));
    if (!headers.has('Content-Type')) headers.set('Content-Type', 'application/json');
    return new global.Response(payload, { ...init, headers: Object.fromEntries(headers) });
  };
}

